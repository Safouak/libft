#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	idx;
	char	*cs;

	idx = 0;
	cs = (char *)s;
	while (n)
	{
		cs[idx] = '\0';
		idx++;
		n--;
	}
}
