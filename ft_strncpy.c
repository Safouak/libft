#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t idx;

	idx = 0;
	while (src[idx] != '\0' && (idx < n))
	{
		dest[idx] = src[idx];
		idx++;
	}
	while (idx < n)
	{
		dest[idx] = '\0';
		idx++;
	}
	return (dest);
}
